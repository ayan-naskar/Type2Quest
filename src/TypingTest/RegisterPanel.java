    private boolean verifyUserName(String username) {
        if(username.indexOf(' ')>=0) {
            JOptionPane.showMessageDialog(this, "User name cannot contain spaces");
            UserNameText.setText("");
            UserNameText.requestFocus();
            return false;
        }
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con1 = DriverManager.getConnection("jdbc:mysql://localhost/Type2Quest","root","ayan123");
            PreparedStatement insert = con1.prepareStatement("select * from playerinfo where USERNAME=(?)");
            insert.setString(1, username);
            
            ResultSet rs = insert.executeQuery();            
            
            if(rs.isBeforeFirst()) {
                JOptionPane.showMessageDialog(this, "User name not available");
                return false;
            }            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(LoginPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        return true;
    }
