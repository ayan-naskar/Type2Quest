    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
        // TODO add your handling code here:
        String username=UserNameText.getText();
        String name=NameText.getText();
        String password=new String(PasswordText.getPassword());
        String confpassword=new String(ConfPasswordText.getPassword());
        
        if(username.trim().length()==0 || name.trim().length()==0 || password.trim().length()==0 || confpassword.trim().length()==0) {
            JOptionPane.showMessageDialog(this, "Fields Cannot be Empty");
            return;
        }
        
        if(!verifyUserName(username)) {
            return;
        }
        
        if(!password.equals(confpassword)) {
            JOptionPane.showMessageDialog(this, "Passwords do not match, please re-enter");
            PasswordText.setText("");
            ConfPasswordText.setText("");
            PasswordText.requestFocus();
            return;
        }
        
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con1 = DriverManager.getConnection("jdbc:mysql://localhost/Type2Quest","root","ayan123");
            PreparedStatement insert = con1.prepareStatement("insert into Playerinfo(username,name,password)values(?,?,?)");
            insert.setString(1, username);
            insert.setString(2, name);
            insert.setString(3, password);
            insert.executeUpdate();            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegisterPanel.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(RegisterPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        LoginPanel lp=new LoginPanel();
        lp.setVisible(true);
        dispose();
    }//GEN-LAST:event_SubmitButtonActionPerformed

